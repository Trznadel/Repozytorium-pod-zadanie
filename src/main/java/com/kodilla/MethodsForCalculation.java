package com.kodilla;

public class MethodsForCalculation {

    public double doAddition(double a, double b) {
        double c = a+b;
        return c;
        System.out.println(c);
    }
    public double doSubstraction(double a, double b) {
        double d = a-b;
        return d;
        System.out.println(d);
    }
}
